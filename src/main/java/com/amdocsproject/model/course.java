package com.amdocsproject.model;

public class course {
	public String cid;
	public String cname;
	public String cdesc;
	public String cfees;
	public String getCid() {
		return cid;
	}
	public void setCid(String cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getCdesc() {
		return cdesc;
	}
	public void setCdesc(String cdesc) {
		this.cdesc = cdesc;
	}
	public String getCfees() {
		return cfees;
	}
	public void setCfees(String cfees) {
		this.cfees = cfees;
	}
	
}
